package scala_beginner.part1_basics

object ValuesObjectTypes extends App {

  val x : Int = 23
  println(x)

  // VALS are immutable

  // TYPE inference

  val name : String = "Hello"

  // semicolon is optional

  val isActive: Boolean = true // or false
  val ch : Char = 'a'
  val age: Int = 90
  val height : Short = 1700
  val distance: Long = 234567890L
  val temperature: Float = 12.6F
  val amount: Double = 29804.03D

  var counter: Int = 4
  counter = 6
  // variable is used for side effects


}
